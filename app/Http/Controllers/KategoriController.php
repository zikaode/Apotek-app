     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $kategori = Kategori::find($id);
        $nama = $kategori->nama;
        if (!$kategori)
            return back();
        $kategori->delete();
        session()->flash('status', "Kategori '{$nama}' Berhasil Dihapus!");
        return back();
    }
